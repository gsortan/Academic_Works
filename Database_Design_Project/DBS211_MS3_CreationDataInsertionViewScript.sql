CREATE TABLE customers (
    custID NUMBER(8) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    firstName VARCHAR(20) DEFAULT NULL,
    lastName VARCHAR(30) DEFAULT NULL,
    phoneNumber NUMBER(10) DEFAULT NULL,
    email VARCHAR(50) DEFAULT NULL,
    address VARCHAR(50) DEFAULT NULL
    );
     
CREATE TABLE employees
(
empID NUMBER(4) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
firstName VARCHAR(20) NOT NULL,
lastName VARCHAR(30) NOT NULL,
userName VARCHAR(30) NOT NULL,
password VARCHAR(20) NOT NULL,
sin NUMBER(9) NOT NULL,
dob DATE NOT NULL,
email VARCHAR(50) NOT NULL,
phoneNumber NUMBER(10) NOT NULL
);

CREATE TABLE orders(
orderID INT GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
custID NUMBER(8) NOT NULL,
empID NUMBER(4) NOT NULL,
status VARCHAR(10) NOT NULL,
dateOfPurchase DATE NOT NULL,
dateShipped DATE DEFAULT NULL,
transactionType VARCHAR(10) NOT NULL,
CONSTRAINT customers_orders_FK FOREIGN KEY(custID)
REFERENCES customers(custID),
CONSTRAINT employees_orders_FK FOREIGN KEY(empID)
REFERENCES employees(empID)
);

CREATE TABLE publishers
(
publisherID NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
companyName VARCHAR(30) NOT NULL,
country CHAR(3) NOT NULL,
contactEmail VARCHAR(50) NOT NULL
);

CREATE TABLE platforms
(
platformID NUMBER(4)GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
platformName VARCHAR(30) NOT NULL
);

CREATE TABLE videogames
(gameID INT GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
productName VARCHAR(30) NOT NULL,
genre VARCHAR(10) NOT NULL,
sellingPrice NUMBER(10,2) NOT NULL,
ageRating CHAR(1) NOT NULL,
stockQuantity INT DEFAULT NULL,
supplierCost NUMBER(10,2) NOT NULL,
publisherID NUMBER(5) NOT NULL,
releaseDate DATE NOT NULL,
platformID NUMBER(4) NOT NULL,
CONSTRAINT publishers_videogames_FK FOREIGN KEY (publisherID)
REFERENCES publishers(publisherID),
CONSTRAINT platforms_videogames_FK FOREIGN KEY(platformID)
REFERENCES platforms(platformID)
);

CREATE TABLE supply_orders
(supplyOrderID INT GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
empID NUMBER(4) NOT NULL,
status VARCHAR(10) NOT NULL,
dateOfPurchase DATE NOT NULL,
storeAddress VARCHAR(50) NOT NULL,
paidPrice NUMBER(10,2) NOT NULL,
dateShipped DATE DEFAULT NULL,
CONSTRAINT employees_supply_orders_FK FOREIGN KEY(empID)
REFERENCES employees(empID)
);

CREATE TABLE order_videogames
(
orderID INT ,
gameID INT,
status VARCHAR(10) NOT NULL,
paidPrice NUMBER(10,2) NOT NULL,
quantity INT NOT NULL,
CONSTRAINT order_videogames_PK PRIMARY KEY (orderID, gameID),
CONSTRAINT orders_order_videogames_FK FOREIGN KEY(orderID)
REFERENCES orders(orderID),
CONSTRAINT videogames_order_videogames_FK FOREIGN KEY(gameID)
REFERENCES videogames(gameID)
);

CREATE TABLE supply_order_videogames
(
supplyOrderID INT,
gameID INT,
status VARCHAR(10) NOT NULL,
paidPrice NUMBER(10,2) NOT NULL,
quantity INT NOT NULL,
CONSTRAINT so_videogames_PK PRIMARY KEY (supplyOrderID, gameID),
CONSTRAINT sorders_sovideogames_FK FOREIGN KEY(supplyOrderID)
REFERENCES supply_orders(supplyOrderID),
CONSTRAINT videogames_sovideogames_FK FOREIGN KEY(gameID)
REFERENCES videoGames(gameID)
);

    
