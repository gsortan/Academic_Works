CREATE VIEW VW_GameQuantityInStock AS
SELECT productName,stockQuantity,platformName
FROM videogames INNER JOIN platforms ON videogames.platformID = platforms.platformID
WHERE stockQuantity IS NOT NULL
ORDER BY stockQuantity;
/* This gets the stock quantities of the items in the store along with the associated console. 
It's all sorted by the lowest first then highest in the list. 
This is useful for the business as by seeing from this report what the lowest stocked items are. 
They can plan accordingly ahead of time for supply orders in terms of what's urgently needed at this point in time
before they run out of it and make estimations. */


CREATE VIEW VW_EmployeeContactList AS
SELECT firstName || ' ' || lastName AS employeeName, email, phoneNumber
FROM employees 
ORDER BY employeeName;
/*This grabs the first and last name from the employees table to concatenate them together
in one field for their full name into a list that's in alphabetical order with their contact info. 
This helps the business keep a head count of how many employees they have hired. It can also 
be useful as a reference list for anyone to really learn all the names of employees of the company. 
Having their contact info in there with email and phone number can help anyone commmunicate and contact them 
if neccessary.*/

CREATE VIEW VW_CustomerOrdersProcessing AS
SELECT orders.orderID, firstName || ' ' || lastName AS customerName, TO_CHAR(dateOfPurchase, 'mm/dd/yyyy') AS purchaseDate, 
TO_CHAR(dateShipped, 'mm/dd/yyyy') AS shipDate, productName, dateShipped - dateOfPurchase AS processingDays
FROM customers INNER JOIN orders ON customers.custID = orders.custID INNER JOIN order_videogames ON orders.orderID = order_videogames.orderID 
INNER JOIN videogames ON videogames.gameID = order_videogames.gameID  
WHERE (orders.status = 'Delivered' OR orders.status = 'Refund') AND (transactionType!='cash' AND (dateShipped - dateOfPurchase)!=0)
ORDER BY processingDays;
/*This takes the orders that have shipped from the orders list and calculates in 
days how long each one took for order processing showing the product names and customer names. It sorts 
it from lowest number of days to highest. By doing this it gives valuable information 
to the company in terms of how efficient the order process is for them. It can help identify areas 
of issue and any long hold ups. As it's very important as well to inform customers how long their 
delivery may take on average for maximum customer satisfication.*/


CREATE VIEW VW_SupplyOrdersProcessing AS
SELECT supply_orders.supplyOrderID, companyName, TO_CHAR(dateOfPurchase, 'mm/dd/yyyy') AS purchaseDate, 
TO_CHAR(dateShipped, 'mm/dd/yyyy') AS shipDate, productName, dateShipped - dateOfPurchase AS processingDays
FROM publishers INNER JOIN videogames ON publishers.publisherID = videogames.publisherID
INNER JOIN supply_order_videogames ON videogames.gameID = supply_order_videogames.gameID
INNER JOIN supply_orders ON supply_orders.supplyOrderID = supply_order_videogames.supplyOrderID  
WHERE supply_orders.status = 'Completed' OR supply_orders.status = 'Shipped' OR supply_orders.status = 'Delayed'
ORDER BY processingDays;
/*This is a view that takes the shipped orders from the supply orders list and 
calculates in days how long each one took for order processing with the product names and company names. 
It sorts it from the lowest number of days to highest. It's good for the company 
to see how effective suppliers are in terms of fufiling orders for them. It can help 
identify if there are any issues with specific suppliers at hand. This is greatly important 
to the business as having stock as soon as possible is a must for the growth of the company. 
If they track far too many lengthy delays in the supply chain ordering process they 
can bring this up with the supplier having issues.*/
